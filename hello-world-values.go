package main

import "fmt"

func main() {
	fmt.Println("hello world")

	// nối chuỗi
	fmt.Println("Nối " + "Chuỗi")

	// tính số int
	fmt.Println("1 + 1 = ", 1+1)

	// tính số float
	fmt.Println("1.2 + 1.3 = ", 1.2+1.3)

	// boolean
	fmt.Println(true && false)

	fmt.Println(true || false)

	fmt.Println(!true)
}
